class Solution {
  public:
    // Function to search a given number in row-column sorted matrix.
    bool searchMatrix(vector<vector<int>> &mat, int x) {
        int l=0,h=mat.size()-1;
        while(l<=h){
            int mid=(l+h)/2;
            if(mat[mid].back()<x)l=mid+1;
            else h=mid-1;
        }
        if(h+1>=mat.size())return 0;
        return binary_search(mat[h+1].begin(),mat[h+1].end(),x);
    }
};

